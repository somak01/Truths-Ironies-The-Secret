

class RealNumberTypes
{
    public static void main(String[] args)
    {
        float floatMin = Float.MIN_VALUE;
        System.out.println("The minimum value of the float type is:\t" + floatMin + "\n");

        float floatMax = Float.MAX_VALUE;
        System.out.println("The maximum value of the float type is:\t" + floatMax + "\n");

        double doubleMin = Double.MIN_VALUE;
        System.out.println("The minimum value of the double type is:\t" + doubleMin + "\n");
        
        double doubleMax = Double.MAX_VALUE;
        System.out.println("The maximum value of the double type is:\t" + doubleMax + "\n");
    }
}