class ObjectType
{
    public static void main(String[] args)
    {
        Object intObject = 1;
        Object floatObject = 1.2f;
        Object doubleObject = 1.2;
        Object charObject ='1';
        Object stringObject = "TwentyTwo";
        System.out.println(intObject  + "\n" + floatObject + "\n" + doubleObject + "\n" + charObject + "\n" + stringObject);
    }
}